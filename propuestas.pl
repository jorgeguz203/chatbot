propuesta(amlo, corrupcion, 'Las propuestas de AMLO para conbatir la corrupcion son:
	1 .- Terminar con la corrupcion                
	2.-  Fondos de corrupcion para desarrollo
	3.-  Reducir el gasto corriente, se refire a la adquisicion de bienes y servicios que realiza el sector publico durante su mandato sin incrementar el patromonio federal
	4.-  cortar  vilegios que hay en el gobierno                       
	5.-  Reducir el gasto corriente para aumentar la inversion publica, que se va a utilizar como capital semilla y se va a complementar con inversion  vada, para impulsar proyectos productivos, crear empleos y asi lograr que haya crecimiento economico en el pais                     
	6.-  Impedir el amiguisimo, el influyentismo, ni el nepotismo                            
	7.-  Acabar con gastos excesivos de funcionarios de gobierno: pensiones para ex presidentes, viajes en helicoptero o aviones  vados, salario de altos funcionarios
	8.-  Poner fin a fraudes electorales y compra de votos').
propuesta(amlo, economia, 'Las propuestas de AMLO para el sector economico son:
	1.- Darle autonomia al Banco de Mexico 
	2.- No habra aumento de impuestos en terminos reales
	3.- Sumir los  vilegios y fueros de los funcionarios publicos
	4.- Descentralizar el gobierno federal
	5.- Rescatar al campo y a sus pobladores
	6.- Fijar precios de garantia para los productos agricolas, producir fertilizantes y distribuir a precios bajos
	7.- Detener la caida de la produccion de energeticos y derivados petroleros
	8.- El Estado desarrollara el poder economico y el fortalecimiento del mercado interno, no aumentaran los impuestos ni la deuda publica
	9.- Duplicar las pensiones a adultos mayores').
propuesta(amlo, desarrollo, 'Las propuestas de AMLO para mejorar el desarrollo son:
	1.- Construir dos pistas en el aeropuerto militar de Santa Lucia para resolver el problema de la saturacion en el actual aeropuerto de la Ciudad de Mexico
	2.- Acceso a internet en todo el territorio nacional
	3.- Desarrollar un tren turistico en la zona maya que recorra la ruta Cancun-Tulum, Calakmul-Palenque
	4.- Crear una zona franca o libre a lo largo de los 3 mil 180 kilometros de frontera con Estados Unidos
	5.- Poner en ejecucion el programa integral de desarrollo en el Istmo de Tehuantepec, desarrollo a Oaxaca, Chiapas, Tabasco y Veracruz
	6.- Llevar agua a Quintana Roo desde Tabasco y Campeche
	7.- Otorgar concesiones sociales utilizando el espectro en diferentes bandas de frecuencia para prestar los servicios de telecomunicaciones moviles de voz y datos').
propuesta(amlo, educacion, 'La propuesta de AMLO para mejorar la educacion es:
